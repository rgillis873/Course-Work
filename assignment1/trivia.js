//Function to load test to page. Parameter a is set to 6 when a random 
//test is loaded
function loadTest(testNumber){
	//Clear the current page
	document.getElementById("testArea").innerHTML = "";
	document.body.style.backgroundImage = "url('Gus.png')";
	
	//Get element where test data will be shown and get selected test index
	let page = document.getElementById("testArea");
	let chosen = document.getElementById("drop");
	let pick = chosen.selectedIndex;
	
	//If a is greater than 5, then a random test is being loaded
	if(testNumber > 5){
		pick = testNumber;
	}
	let br = document.createElement("br");
	page.append(br);
	
	//Create questions for selected test and add it to page
	for(let i = 0;i < testValue[pick].length;i++){
		let question = document.createElement("div");
		question.innerHTML = "</br>" + (i+1) + ". " + testValue[pick][i].question + "</br> </br>";
		question.id = "question"+(i+1);
		question.className = "question";
		page.appendChild(question);
		
		//Randomly sort the possible answers to the current question
		let allOptions = randSort(pick,i);
		
		//Create the answer radio buttons
		for(let j = 0;j< allOptions.length;j++){
			let choices = document.createElement("input");
			choices.type = "radio"
			choices.id = "q" + j + "answer"+j;
			choices.name = "question"+i;
			choices.value = allOptions[j];
			choices.className = "question"+i; 
			
			//Create label for the answer
			let answer = document.createElement("label");
			answer.innerHTML = allOptions[j];
			
			//Append label to radio button and then button to page
			answer.appendChild(choices);
			page.appendChild(answer);
			let br = document.createElement("br");
			page.append(br);
		}
	}
	//Create clear and check test buttons and add to page
	let clear = document.createElement("button");
	clear.id = "clear"
	clear.innerHTML = "Clear Test";
	clear.style ="margin:5px;";
	clear.setAttribute("onclick","javascript: clearTest();");
	page.appendChild(clear);
	let check = document.createElement("button");
	check.innerHTML = "Check Test";
	check.id = "check";
	check.style ="margin:5px;";
	check.setAttribute("onclick","javascript: checkAnswer();");
	page.appendChild(check);
	
}

//Function takes all possible answers for a question and returns them 
//in randomly sorted order
function randSort(testNum,questionNum){
	let wrong = testValue[testNum][questionNum].incorrect_answers;
	let right = testValue[testNum][questionNum].correct_answer;
	
	//Reset the answers if previously called
	if(wrong.length === 4 || wrong.length === 2){
		wrong = wrong.filter(function(value){
			return value !== right; 
		});
	//For questions with 4 possible answers
	}if(wrong.length === 3){
		let randNum1 = Math.floor(Math.random()*4);
		wrong.splice(randNum1,0,right);
		return wrong;
	
	//For true or false questions
	}else if(wrong.length === 1){
		let randNum2 = Math.floor(Math.random()*2);
		wrong.splice(randNum2,0,right);
		return wrong;
	}	
	
}
//Function checks to see if all questions have been answered. If not,
//unanswered questions are highlighted in red. If all are answered, the 
//users score is displayed as well as an appropriate image.Also adds checks
// and x's to users answers
function checkAnswer(){
	let counter = 0;
	let playerScore = 0;
	
	//Determine which test to compare correct answers
	let chosen = document.getElementById("drop");
	let pick = chosen.selectedIndex;
	
	//If random test is chosen
	if(testValue.length > 6){
		pick = 6;
	}
	let potentialAnswers = document.querySelectorAll('input[type ="radio"]:checked' );
	let questions = document.getElementsByClassName("question");
	
	//If not all questions are answered, send alert to user and highlight 
	//unanswered questions
	if(potentialAnswers.length < questions.length){
		alert("Answer all questions before checking your test!");
		for(let i = 0;i<questions.length;i++){
			let allChoices = document.getElementsByClassName("question"+i);
			for(let j = 0;j<allChoices.length;j++){
				let selection = allChoices[j];
				questions[i].style.color = "black";
				if(selection.checked){
					counter = 0;
					break;
				}
				counter ++;
				if(counter === allChoices.length){
					questions[i].style.color = "red";
					counter = 0;
					break;
				}
			}
		}
		return;
	}
	//Remove previous checks and x's if necessary 
	let allImage = document.getElementsByTagName("img");
		for(let i = allImage.length-1;i>=0;i--){
			allImage[i].parentNode.removeChild(allImage[i]);
		}
		
	//Remove previous score if necessary	
	let scoreChange = document.getElementById("finalScore");
	let page = document.getElementById("testArea");
	if(page.childNodes[page.childNodes.length-1] === scoreChange){
		page.removeChild(scoreChange);
	}
	//Add checks to correct answers and x's to incorrect answers 
	for(let r = 0;r<potentialAnswers.length;r++){
		let allImage = document.getElementsByTagName("img");
		questions[r].style.color = "black";
		if(potentialAnswers[r].value === testValue[pick][r].correct_answer){
			playerScore++;
			let imgCheck = document.createElement("img");
			imgCheck.src = "rsz_1rsz_check.png";
			potentialAnswers[r].parentNode.appendChild(imgCheck);
		}else{
			let imgX = document.createElement("img");
			imgX.src = "rsz_2x.png";
			potentialAnswers[r].parentNode.appendChild(imgX);
			continue;
		}
	}
	//Determine users score
	let finalScore = document.createElement("span");
	finalScore.innerHTML = "Score: "+ playerScore + "/" + potentialAnswers.length;
	finalScore.id = "finalScore";
	let score = document.getElementById("testArea");
	
	//If they got atleast half the questions correct set score text to 
	//green and add celebratory photo
	if(playerScore> (potentialAnswers.length/2)-1){
		finalScore.style.color = "green";
		document.body.style.backgroundImage = "url('celebrate.png')";
	
	//If they failed, set score text to red and add dissappointed photo
	}else{
		finalScore.style.color = "red";
		document.body.style.backgroundImage = "url('kbob.png')";
	}
	//Add the score to the page next to the photo
	score.appendChild(finalScore);
	
}

//Clear all changes made by user to test page
function clearTest(){
	
	//Make sure user wants to clear the test
	if(confirm("Are you sure you want to clear all current test answers?")){
		let buttons = document.querySelectorAll('input[type = "radio"]:checked');
		let allImages = document.getElementsByTagName("img");
		
		//Uncheck all buttons and remove check's and x's
		for(let i = buttons.length-1;i >= 0;i--){
			buttons[i].checked = false;
			allImages[i].parentNode.removeChild(allImages[i]);
		}
		//Set all text to black
		let questions = document.getElementsByClassName("question");
		for(let n = 0;n<questions.length;n++){
			questions[n].style.color = "black";
		}
		
		//Remove score
		let scoreChange = document.getElementById("finalScore");
		let page = document.getElementById("testArea");
		if(page.childNodes[page.childNodes.length-1] === scoreChange){
			page.removeChild(scoreChange);
		}
		document.body.style.backgroundImage = "url('Gus.png')";
	}
	
}

//Function sends a XMLHttpRequest to open trivia database to load between 
//10-50 test questions
function loadRandom(){
	//Clear old random test if necessary
	if(testValue.length > 6){
		testValue.pop();
	}
	
	//Determine how many questions to load
	let testLength = document.getElementById("selectLength");
	let url;
	if(testLength.selectedIndex === 0){
		url = "https://opentdb.com/api.php?amount=10"
	}if(testLength.selectedIndex === 1){
		url = "https://opentdb.com/api.php?amount=20"
	}if(testLength.selectedIndex === 2){
		url = "https://opentdb.com/api.php?amount=30"
	}if(testLength.selectedIndex === 3){
		url = "https://opentdb.com/api.php?amount=40"
	}if(testLength.selectedIndex === 4){
		url = "https://opentdb.com/api.php?amount=50"
	}
	
	//Send request
	let http = new XMLHttpRequest();
	http.open("Get",url,true);
	http.onreadystatechange = function(){
		if(http.readyState === 4){
			let convert = JSON.parse(http.responseText);
			let keys = Object.keys(convert);
			let values = Object.values(convert);
			//If response code equals 0, continue to load test
			if(values[0] === 0){
				allTest.results = values[1];
				testValue.push(values[1]);
				loadTest(6);
			}
			else{
				alert("Request Failed");
				document.getElementById("testArea").innerHTML = "";
			}
		}
			
	};
	http.send();
}

//Function to filter questions by difficulty
function setDifficulty(){
	
	//Determine which difficulty was chosen
	let difficultChoice = document.getElementById("selectDifficulty");
	let setting = difficultChoice.selectedIndex;
	
	//Determine which test is being filtered
	let testChoice = document.getElementById("drop");
	let testNum = testChoice.selectedIndex;
	let testType = document.getElementById("testType");
	
	//If test type is set to random and a random test has been loaded,
	//filter the random test
	if(testType.selectedIndex == 1 && testValue.length>6){
		testNum = 6;
	}
	//Filter for all
	if(setting === 0){
		testValue = Object.values(allTest);
	
	//Filter for easy	
	}else if(setting === 1){
		testValue = Object.values(allTest);
		testValue[testNum] = testValue[testNum].filter(function(value){
			return value.difficulty === "easy";
		});
	
	//Filter for medium
	}else if(setting === 2){
		testValue = Object.values(allTest);
		testValue[testNum] = testValue[testNum].filter(function(value){
			return value.difficulty === "medium";
		});
	
	//Filter for hard
	}else{
		testValue = Object.values(allTest);
		testValue[testNum] = testValue[testNum].filter(function(value){
			return value.difficulty === "hard";
		});
	}
	//If random test is being filtered, load random test
	if(testNum > 5){
		loadTest(6);
	//Load whichever normal test was chosen
	}else{
		loadTest(1);
	}
}

